    private void jBVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBVerActionPerformed
        for (int i = 0; i < tabla.getRowCount(); i++) {
            tabla.removeRow(i);
            i -= 1;
        }
        ArrayList alumnos = Conexion.mostrarAlumnos();
        Iterator it = alumnos.iterator();
        while (it.hasNext()) {
            Alumno alu = (Alumno) it.next();
            String[] fila = new String[5];
            fila[0] = String.valueOf(alu.getId());
            fila[1] = alu.getNombre();
            fila[2] = alu.getApellidos();
            fila[3] = String.valueOf(alu.getFechaNaciemiento());
            fila[4] = String.valueOf(alu.getNota());
            tabla.addRow(fila);
            jTableAlumnos.setModel(tabla);
        }
    }//GEN-LAST:event_jBVerActionPerformed

